package application;

public class ProductBean {
	
	int productId;
	String productName;
	int productSellingPrice;
	String productSectionName;
	
	public int getProductId() {
		return productId;
	}

	public void setProductId(int productId) {
		this.productId = productId;
	}

	public String getProductName() {
		return productName;
	}
	
	public void setProductName(String productName) {
		this.productName = productName;
	}
	
	public int getProductSellingPrice() {
		return productSellingPrice;
	}
	
	public void setProductSellingPrice(int productSellingPrice) {
		this.productSellingPrice = productSellingPrice;
	}
	
	public String getProductSectionName() {
		return productSectionName;
	}
	
	public void setProductSectionName(String productSectionName) {
		this.productSectionName = productSectionName;
	}
	
	
}
